#include "shell.h"

int		ft_isalpha(int c)
{
	if (ft_tolower(c) != c || ft_toupper(c) != c)
		return (1);
	return (0);
}

int		ft_isalphanum(int c)
{
	if ((c >= 48 && c <= 57) || (c >= 65 && c <= 90) || (c >= 97 && c <= 122)
		|| (c == '.') || (c == '/') || (c == ',') || (c == ':')
		|| (c == '!') || (c == '?') || (c == '@') || (c == '#')
		|| (c == '$') || (c == '%') || (c == '^') || (c == '*')
		|| (c == ')') || (c == '(') || (c == '_') || (c == '=')
		|| (c == '[') || (c == ']') || (c == '{') || (c == '}')
		|| (c == '`') || (c == '~') || (c == '\\'))
		return (1);
	return (0);
}
