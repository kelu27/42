#include <string.h>

void	*ft_memccpy(void *s1, const void *s2, int c, size_t n)
{
	unsigned char	*p1;
	unsigned char	*p2;
	size_t			i;

	i = 0;
	p1 = (unsigned char *)s1;
	p2 = (unsigned char *)s2;
	while (i < n)
	{
		*(p1 + i) = *(p2 + i);
		if (*(p2 + i) == (unsigned char)c)
			return (p1 + i + 1);
		i++;
	}
	return (NULL);
}
