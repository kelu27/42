#include <mlx.h>
#include <unistd.h>
#include <stdlib.h>
#include "fdf.h"

int		calcx(int x, int y)
{
	x = (x * 30) + 400;
	y = (y * 30) - 300;
	return ((0.9 * x - 0.9 * y) + 100);
}

int		calcy(int x, int y, int z)
{
	x = (x * 30) + 400;
	y = (y * 30) - 300;
	return (((-z * 2) + (0.9 / 2) * x + (0.9 / 2) * y) + 100);
}
